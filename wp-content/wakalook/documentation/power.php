</p>



<h2 id="subscript">添字</h2>



<!-- 添字の一覧の画像を挿入 -->

<img src="<?php bloginfo("template_url"); ?>/images/docs/subscript.png">



<p>

    添字を入力することが出来ます。<br>

    また、前に数字が存在していない場合でも、添字を利用することで、「下付き文字」を入力することが出来ます。<br>

    ボタンを押すことで、添字入力モードに入ることが出来ます。<br>

    これで、数式を入力することにより、添字部分に入力した数式を格納することが出来ます。

    「<span class="inline">OK</span>」ボタンを押すことにより、入力を完了することが出来ます。

</p>



<h3>キーボード入力について</h3>



<p>

    添字について、キーボードから入力することが出来ます。<br>

