<p>

    指数を入力することが出来ます。<br>

    また、前に数字が存在していない場合でも、指数を利用することで、「上付き文字」を入力することが出来ます。<br>

    ボタンを押すことで、指数入力モードに入ることが出来ます。<br>

    これで、数式を入力することにより、指数部分に入力した数式を格納することが出来ます。<br>

    「<span class="inline">OK</span>」ボタンを押すことにより、入力を完了することが出来ます。

</p>



