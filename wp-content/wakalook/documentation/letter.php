<h2 id="howto">どのように使うか</h2>



<p>

    ここでは、アルファベットやギリシャ文字等の文字を入力することが出来ます。<br>

    それぞれ、ボタンを押すことで、その内容がそのまま入力されます。

</p>



<h3>キーボード入力について</h3>



<p>

    アルファベットについては、キーボードから入力することが出来ます。<br>

    ローマン体のものか否かで入力方法が変わります。

</p>



<h4>ローマン体である場合</h4>



<p>

    プレビュー下部に存在するボタンで、「<span class="inline">斜字にする</span>」というボタンがある場合は、これを押してください。<br>

    ない場合はそのままで構いません。<br>

    その後に、キーボードから入力したいアルファベットを入力してください。<br>

    大文字を入力する場合は、「<span class="inline">Shift</span>」キーを押しながらアルファベットを押すことで、入力できます。

</p>



<h4>ローマン体でない場合</h4>



<p>

    プレビュー下部に存在するボタンで、「<span class="inline">斜字にしない</span>」というボタンがある場合は、これを押してください。<br>

    ない場合はそのままで構いません。<br>

    その後に、キーボードから入力したいアルファベットを入力してください。<br>

    大文字を入力する場合は、「<span class="inline">Shift</span>」キーを押しながらアルファベットを押すことで、入力できます。

</p>



