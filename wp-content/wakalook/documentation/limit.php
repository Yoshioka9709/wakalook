<h2 id="summary">概要</h2>



<p>

    大学受験において微積分は重要な分野の一つであり、大学に入ってもなおその利用頻度は褪せることを知りません。<br>

    その微分を定義する場合に使われるのが、極限の概念です。<br>

    微分自体は現行の指導要領では数Ⅱで履修しますが、微分というものをよく知るためには、数Ⅲで習う極限を身に付けなければなりません。<br>

    当ツールでは、そんな「極限」も入力することが出来ます。

</p>



<h2 id="howto">入力の仕方</h2>



<!-- 極限のカテゴリーの画像を挿入 -->

<img src="<?php bloginfo("template_url"); ?>/images/docs/limit.png">



<p>

    極限のカテゴリーにあるボタンを押してください。<br>

    このとき、極限入力モードに入ります。<br>

    極限入力モードで数式を入力すれば、指定された場所に数式が入力されます。<br>

    また、初期状態では極限を取る変数の部分を入力することが出来ますが、「<span class="inline">入力項目切り替え</span>」ボタンを押すことにより、極限を求める数式を入力できるようになります。<br>

    なお、「<span class="inline">OK</span>」ボタンを押すことにより、入力を完了することが出来ます。

</p>