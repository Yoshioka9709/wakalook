<h2 id="howto">どのように使うか</h2>



<p>

    ここでは、単位を入力することが出来ます。<br>

    それぞれ、ボタンを押すことで、その内容がそのまま入力されます。

</p>



<h3>キーボード入力について</h3>



<p>

    プレビュー下部にある「<span class="inline">斜字にしない</span>」ボタンを押してください。<br>

    その後で、入力したい単位（アルファベット）を直接入力すると、単位をキーボードから入力することが出来ます。

</p>



<div class="infoBlock">

    <div class="infoTitle">info</div>

    <div class="infoMessage">

        基本的に単位は斜字にせずに入力することが多いので、上でもそのように述べています。<br>

        なお、「<span class="inline">斜字にする</span>」というボタンがある場合は、すでに入力するアルファベットが斜字でない（ローマン体）ものになっているので、<br>

        そのまま入力していただけます。

    </div>

</div>



