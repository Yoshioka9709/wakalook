<h2 id="graphic">グラフィックソフトとしてのWakalook</h2>



<p>

    Wakalookには、ペンおよび消しゴム、そしてペンに関しては色を変えることもできます。<br>

    また、ペンの太さも細かく調整することができるので、塗りつぶしも簡単に行うことが出来ます。

</p>



<p>

    また、タッチペンでも動作することを確認しています。<br>

    これより、タブレットを用いれば、マウスで描くよりも簡単に、細かい画像を描くことが出来ます。<br>

    この場合、画面のスクロールは二本指で行うことが出来ます。

</p>



<h2 id="memo">メモとしてのWakalook</h2>



<p>

    Wakalookには、ペンや消しゴムだけでなく、テキストを入力できる場所もあります。<br>

    このテキストは、好きなように色やサイズを変えることもできます。<br>

    テキストボタンを利用することで、キャンバスの好きな場所にメモを貼り付けることが出来ます。

</p>



<p>

    さらにWakalookには、パソコンやタブレットの中にある画像ファイルを、貼り付けられる機能があります。<br>

    貼り付ける画像の大きさは、自由に変えることが出来ます。<br>

    これにより、メモで画像が必要になっても、「Wakalook」なら簡単に使うことが出来ます。

</p>



<p>

    最後にWakalookには、共有機能があります。これを使うことで、必要な人に簡単にメモを共有出来ます。<br>

    個人専用でも他の人との共有用でも、好きなようにご利用いただけます。<br>

    ※個人情報など重要なことは書き込まないでください。また、共有相手もアカウントを作成し、ログインする必要があります。

</p>



<h2 id="notebook">ノートとしてのWakalook</h2>



<p>

    Wakalookには、簡単に数式を入力できる「数式入力ダイアログ」が存在します。<br>

    これを用いることにより、勉強で必要な数式を簡単に入力することが出来ます。

</p>



<p>

    手描きもできるし、テキストも入力できるし、きれいな数式を簡単に入力することもできます。<br>

    つまり、これを使えば、「手描きのように」電子機器上で、簡単にノートを取ることができるということです。

</p>